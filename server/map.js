exports.addSpawnPoint = (mapChoice) => {
    const { maps } = getState()
    const map = maps[mapChoice]
    const randRow = Math.floor(Math.random() * map.length)
    const randCol = Math.floor(Math.random() * map[0].length)

    for (let row = randRow; row < map.length; ++row)
        for (let col = randCol; col < map[0].length; ++col)
            if (map[row][col] == '0') return (map[row][col] = 'p')

    for (let row = 0; row <= randRow; ++row)
        for (let col = 0; col < map[0].length; ++col)
            if (map[row][col] == '0') return (map[row][col] = 'p')
}
